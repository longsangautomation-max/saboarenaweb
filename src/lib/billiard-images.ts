/**
 * Lấy ảnh ngẫu nhiên theo category
 */
export function getRandomImage(category: keyof typeof BILLIARD_IMAGES): string {
  const images = BILLIARD_IMAGES[category];
  if (!images || images.length === 0) {
    return BILLIARD_IMAGES.tournaments[0].url;
  }
  const randomIndex = Math.floor(Math.random() * images.length);
  return images[randomIndex].url;
}

/**
 * Lấy ảnh theo index
 */
export function getImageByIndex(
  category: keyof typeof BILLIARD_IMAGES, 
  index: number = 0
): string {
  const images = BILLIARD_IMAGES[category];
  if (!images || images.length === 0) {
    return BILLIARD_IMAGES.tournaments[0].url;
  }
  const safeIndex = index % images.length;
  return images[safeIndex].url;
}

/**
 * Map category tin tức -> category ảnh
 */
export function getCategoryImages(newsCategory: string): string[] {
  const mapping: Record<string, keyof typeof BILLIARD_IMAGES> = {
    tournament: 'tournaments',
    players: 'players',
    guide: 'techniques',
    club: 'clubs',
    interview: 'players',
    regulation: 'generic',
    match: 'matches'
  };
  
  const imageCategory = mapping[newsCategory] || 'tournaments';
  return BILLIARD_IMAGES[imageCategory].map(img => img.url);
}

/**
 * Lấy ảnh cover phù hợp với category tin tức
 */
export function getCoverImageForNews(newsCategory: string): string {
  const categoryMapping: Record<string, keyof typeof BILLIARD_IMAGES> = {
    tournament: 'tournaments',
    players: 'players',
    guide: 'techniques',
    club: 'clubs',
    interview: 'players',
    regulation: 'generic',
    match: 'matches'
  };
  
  const imageCategory = categoryMapping[newsCategory] || 'tournaments';
  return getRandomImage(imageCategory);
}
